#include <iostream>

using namespace std;

int main()
{
  cout << "4 < 9 \t" << (4 < 9) << endl;
  cout << "4 < 4 \t" << (4 < 4) << endl;
  cout << "4 < 1 \t" << (4 < 1) << endl;

  cout << endl << endl;

  cout << "4 > 9 \t" << (4 > 9) << endl;
  cout << "4 > 4 \t" << (4 > 4) << endl;
  cout << "4 > 1 \t" << (4 > 1) << endl;

  cout << endl << endl;

  cout << "4 <= 9 \t" << (4 <= 9) << endl;
  cout << "4 <= 4 \t" << (4 <= 4) << endl;
  cout << "4 <= 1 \t" << (4 <= 1) << endl;

  cout << endl << endl;

  cout << "4 >= 9 \t" << (4 >= 9) << endl;
  cout << "4 >= 4 \t" << (4 >= 4) << endl;
  cout << "4 >= 1 \t" << (4 >= 1) << endl;

  cout << endl << endl;

  cout << "4 == 9 \t" << (4 == 9) << endl;
  cout << "4 == 4 \t" << (4 == 4) << endl;
  cout << "4 == 1 \t" << (4 == 1) << endl;

  cout << endl << endl;

  cout << "4 != 9 \t" << (4 != 9) << endl;
  cout << "4 != 4 \t" << (4 != 4) << endl;
  cout << "4 != 1 \t" << (4 != 1) << endl;



  return 0;
}
